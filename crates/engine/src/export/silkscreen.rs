use crate::board::BoardText;
use crate::ir::geometry::Point;

use super::ExportError;

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct SilkscreenStroke {
    pub from: Point,
    pub to: Point,
    pub width_nm: i64,
}

pub fn render_silkscreen_text_strokes(
    text: &BoardText,
) -> Result<Vec<SilkscreenStroke>, ExportError> {
    let mut strokes = Vec::new();
    let scale_nm = text.height_nm / 5;
    let advance_nm = scale_nm * 4;
    let mut cursor_x = 0_i64;
    for ch in text.text.chars() {
        for ((x1, y1), (x2, y2)) in glyph_strokes(ch)? {
            strokes.push(SilkscreenStroke {
                from: rotate_text_point(
                    text.position,
                    text.rotation,
                    cursor_x + x1 * scale_nm,
                    y1 * scale_nm,
                ),
                to: rotate_text_point(
                    text.position,
                    text.rotation,
                    cursor_x + x2 * scale_nm,
                    y2 * scale_nm,
                ),
                width_nm: text.stroke_width_nm,
            });
        }
        cursor_x += advance_nm;
    }
    Ok(strokes)
}

fn rotate_text_point(origin: Point, rotation_deg: i32, x_nm: i64, y_nm: i64) -> Point {
    let radians = f64::from(rotation_deg).to_radians();
    let x = x_nm as f64;
    let y = y_nm as f64;
    let rotated_x = x * radians.cos() - y * radians.sin();
    let rotated_y = x * radians.sin() + y * radians.cos();
    Point {
        x: origin.x + rotated_x.round() as i64,
        y: origin.y + rotated_y.round() as i64,
    }
}

type GlyphStroke = ((i64, i64), (i64, i64));

fn glyph_strokes(ch: char) -> Result<&'static [GlyphStroke], ExportError> {
    let glyph = match ch {
        ' ' => &[][..],
        '-' => &[((0, 2), (2, 2))][..],
        '_' => &[((0, 0), (2, 0))][..],
        '.' => &[((1, 0), (1, 0))][..],
        '/' => &[((0, 0), (2, 4))][..],
        '+' => &[((1, 0), (1, 4)), ((0, 2), (2, 2))][..],
        '0' => &[
            ((0, 0), (0, 4)),
            ((0, 4), (2, 4)),
            ((2, 4), (2, 0)),
            ((2, 0), (0, 0)),
        ][..],
        '1' => &[((1, 0), (1, 4))][..],
        '2' => &[
            ((0, 4), (2, 4)),
            ((2, 4), (2, 2)),
            ((2, 2), (0, 2)),
            ((0, 2), (0, 0)),
            ((0, 0), (2, 0)),
        ][..],
        '3' => &[
            ((0, 4), (2, 4)),
            ((2, 4), (2, 0)),
            ((0, 2), (2, 2)),
            ((0, 0), (2, 0)),
        ][..],
        '4' => &[((0, 4), (0, 2)), ((0, 2), (2, 2)), ((2, 4), (2, 0))][..],
        '5' => &[
            ((2, 4), (0, 4)),
            ((0, 4), (0, 2)),
            ((0, 2), (2, 2)),
            ((2, 2), (2, 0)),
            ((2, 0), (0, 0)),
        ][..],
        '6' => &[
            ((2, 4), (0, 4)),
            ((0, 4), (0, 0)),
            ((0, 0), (2, 0)),
            ((2, 0), (2, 2)),
            ((2, 2), (0, 2)),
        ][..],
        '7' => &[((0, 4), (2, 4)), ((2, 4), (1, 0))][..],
        '8' => &[
            ((0, 0), (0, 4)),
            ((0, 4), (2, 4)),
            ((2, 4), (2, 0)),
            ((2, 0), (0, 0)),
            ((0, 2), (2, 2)),
        ][..],
        '9' => &[
            ((2, 0), (2, 4)),
            ((2, 4), (0, 4)),
            ((0, 4), (0, 2)),
            ((0, 2), (2, 2)),
            ((2, 0), (0, 0)),
        ][..],
        'A' => &[
            ((0, 0), (0, 4)),
            ((2, 0), (2, 4)),
            ((0, 4), (2, 4)),
            ((0, 2), (2, 2)),
        ][..],
        'B' => &[
            ((0, 0), (0, 4)),
            ((0, 4), (2, 4)),
            ((2, 4), (2, 2)),
            ((2, 2), (0, 2)),
            ((2, 2), (2, 0)),
            ((2, 0), (0, 0)),
        ][..],
        'C' => &[((2, 4), (0, 4)), ((0, 4), (0, 0)), ((0, 0), (2, 0))][..],
        'D' => &[
            ((0, 0), (0, 4)),
            ((0, 4), (2, 3)),
            ((2, 3), (2, 1)),
            ((2, 1), (0, 0)),
        ][..],
        'E' => &[
            ((2, 4), (0, 4)),
            ((0, 4), (0, 0)),
            ((0, 2), (2, 2)),
            ((0, 0), (2, 0)),
        ][..],
        'F' => &[((0, 0), (0, 4)), ((0, 4), (2, 4)), ((0, 2), (2, 2))][..],
        'G' => &[
            ((2, 4), (0, 4)),
            ((0, 4), (0, 0)),
            ((0, 0), (2, 0)),
            ((2, 0), (2, 2)),
            ((2, 2), (1, 2)),
        ][..],
        'H' => &[((0, 0), (0, 4)), ((2, 0), (2, 4)), ((0, 2), (2, 2))][..],
        'I' => &[((0, 4), (2, 4)), ((1, 4), (1, 0)), ((0, 0), (2, 0))][..],
        'J' => &[((0, 4), (2, 4)), ((1, 4), (1, 0)), ((1, 0), (0, 0))][..],
        'K' => &[((0, 0), (0, 4)), ((2, 4), (0, 2)), ((0, 2), (2, 0))][..],
        'L' => &[((0, 4), (0, 0)), ((0, 0), (2, 0))][..],
        'M' => &[
            ((0, 0), (0, 4)),
            ((0, 4), (1, 2)),
            ((1, 2), (2, 4)),
            ((2, 4), (2, 0)),
        ][..],
        'N' => &[((0, 0), (0, 4)), ((0, 4), (2, 0)), ((2, 0), (2, 4))][..],
        'O' => &[
            ((0, 0), (0, 4)),
            ((0, 4), (2, 4)),
            ((2, 4), (2, 0)),
            ((2, 0), (0, 0)),
        ][..],
        'P' => &[
            ((0, 0), (0, 4)),
            ((0, 4), (2, 4)),
            ((2, 4), (2, 2)),
            ((2, 2), (0, 2)),
        ][..],
        'Q' => &[
            ((0, 0), (0, 4)),
            ((0, 4), (2, 4)),
            ((2, 4), (2, 0)),
            ((2, 0), (0, 0)),
            ((1, 1), (2, 0)),
        ][..],
        'R' => &[
            ((0, 0), (0, 4)),
            ((0, 4), (2, 4)),
            ((2, 4), (2, 2)),
            ((2, 2), (0, 2)),
            ((0, 2), (2, 0)),
        ][..],
        'S' => &[
            ((2, 4), (0, 4)),
            ((0, 4), (0, 2)),
            ((0, 2), (2, 2)),
            ((2, 2), (2, 0)),
            ((2, 0), (0, 0)),
        ][..],
        'T' => &[((0, 4), (2, 4)), ((1, 4), (1, 0))][..],
        'U' => &[((0, 4), (0, 0)), ((0, 0), (2, 0)), ((2, 0), (2, 4))][..],
        'V' => &[((0, 4), (1, 0)), ((1, 0), (2, 4))][..],
        'W' => &[
            ((0, 4), (0, 0)),
            ((0, 0), (1, 2)),
            ((1, 2), (2, 0)),
            ((2, 0), (2, 4)),
        ][..],
        'X' => &[((0, 4), (2, 0)), ((0, 0), (2, 4))][..],
        'Y' => &[((0, 4), (1, 2)), ((2, 4), (1, 2)), ((1, 2), (1, 0))][..],
        'Z' => &[((0, 4), (2, 4)), ((2, 4), (0, 0)), ((0, 0), (2, 0))][..],
        'a'..='z' => return glyph_strokes(ch.to_ascii_uppercase()),
        _ => return Err(ExportError::UnsupportedSilkscreenTextCharacter(ch)),
    };
    Ok(glyph)
}
